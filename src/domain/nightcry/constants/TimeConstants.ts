/**
 * 夜泣きイベントの時間管理定数
 *
 * すべての時間は**ゲーム内時間**（ミリ秒）で定義されています。
 * ただし、RESTART_CRYING_DELAY のみ現実時間（ミリ秒）です。
 */

export const NightCryTimeConstants = {
  // === 各アクションの完了時間（ゲーム内時間、ミリ秒） ===
  /** 遊ぶアクション: 15分で満足度Max（現実時間30秒） */
  PLAYING_DURATION: 15 * 60 * 1000, // 15分

  /** 撫でるアクション: 15分で満足度Max（現実時間30秒） */
  PETTING_DURATION: 15 * 60 * 1000, // 15分

  /** 無視するアクション: 15分で諦め度Max（現実時間30秒） */
  IGNORING_DURATION: 15 * 60 * 1000, // 15分

  /** 締め出しアクション: 15分で諦め度Max（現実時間30秒） */
  LOCKED_OUT_DURATION: 15 * 60 * 1000, // 15分

  // === 再び鳴き始めるまでの時間（現実時間、ミリ秒） ===
  /**
   * 満足度・諦め度が 1.0 未満の場合、再び鳴き始めるまでの待機時間
   * 注: これは現実時間なので、GameTimeManager を経由せず直接使用する
   */
  RESTART_CRYING_DELAY: 10 * 1000, // 10秒（調整可能）

  // === 時間経過速度（デバッグ用） ===
  /**
   * 全体の時間スケール
   * 1.0 = 通常速度、2.0 = 2倍速、0.5 = 半分速
   * 30.0 = ゲーム内15分を現実30秒で進める（900秒 / 30秒 = 30倍速）
   */
  TIME_SCALE: 30.0,

  // === 個別アクションの時間スケール（さらに微調整したい場合） ===
  /** 遊ぶアクションの時間スケール */
  PLAYING_TIME_SCALE: 1.0,

  /** 撫でるアクションの時間スケール */
  PETTING_TIME_SCALE: 1.0,

  /** 無視するアクションの時間スケール */
  IGNORING_TIME_SCALE: 1.0,

  /** 締め出しアクションの時間スケール */
  LOCKED_OUT_TIME_SCALE: 1.0,
} as const;
